/**
 * setup/global-setup.js
 *
 * Playwright global setup вЂ” runs ONCE before the entire test suite.
 *
 * Responsibilities:
 *  1. Verify the backend is reachable
 *  2. Verify the frontend is reachable
 *  3. Validate that all test user credentials can actually log in
 *
 * If any check fails the suite stops immediately with a clear message.
 * Run "npm run seed" first if users are missing.
 */
require('dotenv').config({ path: '.env.local' });

const env         = require('../config/env');
const { apiGet, apiPost } = require('../helpers/api-client');

async function globalSetup() {
  console.log('\nв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓ');
  console.log('  рџ”§  GLOBAL SETUP');
  console.log('в”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓ\n');

  // в”Ђв”Ђ 1. Backend health в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
  console.log(`рџ“Ў  Backend  в†’  ${env.apiUrl}`);
  let backendOk = false;
  try {
    const res = await apiGet('/assets');
    backendOk = res.status === 200;
  } catch (err) {
    // fetch throws on network error
  }

  if (!backendOk) {
    throw new Error(
      `\nвќЊ  Backend not reachable at ${env.apiUrl}\n` +
      `    Make sure the Spring Boot server is running on port 8080.\n`
    );
  }
  console.log('    вњ…  Backend is up\n');

  // в”Ђв”Ђ 2. Validate test user credentials в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
  console.log('рџ”‘  Validating test credentials...\n');

  const checks = [
    { label: 'MANAGER',    creds: env.users.manager },
    { label: 'TECHNICIAN', creds: env.users.technician },
    { label: 'USER',       creds: env.users.user },
    { label: 'ADMIN',      creds: env.users.admin },
  ];

  for (const { label, creds } of checks) {
    const res = await apiPost('/auth/login', {
      email:    creds.email,
      password: creds.password,
    });

    if (res.status !== 200 || !res.data?.token) {
      throw new Error(
        `\nвќЊ  Login failed for ${label} (${creds.email})\n` +
        `    HTTP ${res.status}\n\n` +
        `    в–¶  Fix: run  npm run seed  to create test users.\n`
      );
    }

    console.log(`    вњ…  ${label.padEnd(12)} ${creds.email}`);
  }

  console.log('\nв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓ');
  console.log('  вњ…  All systems ready. Running tests...');
  console.log('в”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓ\n');
}

module.exports = globalSetup;
